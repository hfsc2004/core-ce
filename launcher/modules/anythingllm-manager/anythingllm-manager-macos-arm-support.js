const { exec } = require('child_process');
const path = require('path');
const fs = require('fs');
const { promisify } = require('util');

const execPromise = promisify(exec);

async function killProcessGroup(proc, name) {
  if (!proc) return;

  const pid = proc.pid;
  console.log(`[AnythingLLM macOS ARM] Killing ${name} process group (PID: ${pid})...`);

  try {
    try {
      process.kill(-pid, 'SIGTERM');
      console.log(`[AnythingLLM macOS ARM] Killed ${name} process group ${pid}`);
    } catch (_groupErr) {
      if (!proc.killed) {
        proc.kill('SIGTERM');
        console.log(`[AnythingLLM macOS ARM] Killed ${name} process ${pid}`);
      }
    }

    await new Promise((resolve) => setTimeout(resolve, 500));

    try {
      process.kill(-pid, 'SIGKILL');
    } catch (_err) {
      // already dead
    }
  } catch (err) {
    console.error(`[AnythingLLM macOS ARM] Error killing ${name}:`, err.message);
  }
}

async function killRemainingAnythingLLMProcesses(backendPort, frontendPort) {
  console.log('[AnythingLLM macOS ARM] Cleaning up remaining AnythingLLM processes...');

  try {
    await execPromise('pkill -f "anythingllm.*yarn" || true');
  } catch (_err) {
    // no-op
  }

  try {
    await execPromise('pkill -f "anythingllm.*(server|frontend)" || true');
  } catch (_err) {
    // no-op
  }

  if (backendPort) {
    await killProcessesOnPort(backendPort, 'backend');
  }

  if (frontendPort) {
    await killProcessesOnPort(frontendPort, 'frontend');
  }
}

async function killProcessesOnPort(port, tag) {
  try {
    const { stdout } = await execPromise(`lsof -ti:${port}`);
    const pids = stdout.trim().split('\n').filter(Boolean);
    for (const pid of pids) {
      try {
        process.kill(parseInt(pid, 10), 'SIGTERM');
        console.log(`[AnythingLLM macOS ARM] Killed process on ${tag} port ${port} (PID: ${pid})`);
      } catch (_err) {
        // already dead
      }
    }
  } catch (_err) {
    // port not in use
  }
}

function createEnvFiles(anythingPath, ollamaPort, serverPort, clientPort) {
  const rootEnvPath = path.join(anythingPath, '.env');
  const serverEnvPath = path.join(anythingPath, 'server', '.env');
  const serverEnvDevPath = path.join(anythingPath, 'server', '.env.development');
  const frontendEnvPath = path.join(anythingPath, 'frontend', '.env');

  const serverEnvDevContent = `# Pseudo Science Fiction - Dynamic Port Configuration
# Generated by anythingllm-manager - DO NOT EDIT MANUALLY
SERVER_PORT=${serverPort}
STORAGE_DIR=${path.join(anythingPath, 'server', 'storage')}

# Ollama Configuration - CRITICAL for connecting to our Ollama instance!
# We set ALL possible env var names that AnythingLLM might check
OLLAMA_BASE_PATH=http://localhost:${ollamaPort}
OLLAMA_BASE_URL=http://localhost:${ollamaPort}
OLLAMA_API_BASE_PATH=http://localhost:${ollamaPort}
OLLAMA_HOST=http://localhost:${ollamaPort}
LLM_PROVIDER=ollama

# Disable telemetry
DISABLE_TELEMETRY=true
DO_NOT_TRACK=1
TELEMETRY_DISABLED=1
NEXT_TELEMETRY_DISABLED=1
NO_UPDATE_NOTIFIER=1
`;

  const serverEnvContent = `# Pseudo Science Fiction - Dynamic Port Configuration
# Generated by anythingllm-manager - DO NOT EDIT MANUALLY

# Ollama Configuration
OLLAMA_BASE_URL=http://localhost:${ollamaPort}
LLM_PROVIDER=ollama
EMBEDDING_MODEL_PREF=nomic-embed-text

# Server Configuration
SERVER_PORT=${serverPort}

# Storage - Must point to server/storage where anythingllm.db lives!
STORAGE_DIR=${path.join(anythingPath, 'server', 'storage')}

# Disable telemetry
DISABLE_TELEMETRY=true
DO_NOT_TRACK=1
TELEMETRY_DISABLED=1
NEXT_TELEMETRY_DISABLED=1
NO_UPDATE_NOTIFIER=1
`;

  const frontendEnvContent = `# Pseudo Science Fiction - Dynamic Port Configuration
# Generated by anythingllm-manager - DO NOT EDIT MANUALLY

# CRITICAL: This tells the frontend where to find the backend API
VITE_API_BASE=http://localhost:${serverPort}/api
VITE_DISABLE_TELEMETRY=true
DO_NOT_TRACK=1
NEXT_TELEMETRY_DISABLED=1
`;

  try {
    fs.writeFileSync(rootEnvPath, serverEnvContent, 'utf8');
    console.log('[AnythingLLM macOS ARM] Created root .env');

    if (fs.existsSync(path.join(anythingPath, 'server'))) {
      fs.writeFileSync(serverEnvPath, serverEnvContent, 'utf8');
      console.log('[AnythingLLM macOS ARM] Created server/.env');

      fs.writeFileSync(serverEnvDevPath, serverEnvDevContent, 'utf8');
      console.log(`[AnythingLLM macOS ARM] Created server/env.development (SERVER_PORT=${serverPort})`);
    }

    if (fs.existsSync(path.join(anythingPath, 'frontend'))) {
      fs.writeFileSync(frontendEnvPath, frontendEnvContent, 'utf8');
      console.log(`[AnythingLLM macOS ARM] Created frontend/.env (VITE_API_BASE=http://localhost:${serverPort}/api)`);
    }

    return true;
  } catch (err) {
    console.error('[AnythingLLM macOS ARM] Failed to create .env files:', err.message);
    return false;
  }
}

async function updateOllamaUrlInDatabase(anythingPath, ollamaPort) {
  const dbPath = path.join(anythingPath, 'server', 'storage', 'anythingllm.db');

  if (!fs.existsSync(dbPath)) {
    console.log('[AnythingLLM macOS ARM] Database not found, skipping Ollama URL update');
    return false;
  }

  const ollamaUrl = `http://localhost:${ollamaPort}`;

  try {
    const insertQuery = `INSERT OR REPLACE INTO system_settings (label, value, createdAt, lastUpdatedAt) VALUES ('OllamaLLMBasePath', '${ollamaUrl}', datetime('now'), datetime('now'));`;

    try {
      await execPromise(`sqlite3 "${dbPath}" "${insertQuery}"`);
      console.log(`[AnythingLLM macOS ARM] ✅ Set OllamaLLMBasePath in database to ${ollamaUrl}`);
      return true;
    } catch (sqliteErr) {
      console.log('[AnythingLLM macOS ARM] sqlite3 command failed:', sqliteErr.message);

      const scriptPath = path.join(anythingPath, 'psf-update-ollama-url.cjs');
      const scriptContent = `
const Database = require('better-sqlite3');
const db = new Database('${dbPath}');
try {
  const stmt = db.prepare("INSERT OR REPLACE INTO system_settings (label, value, createdAt, lastUpdatedAt) VALUES (?, ?, datetime('now'), datetime('now'))");
  stmt.run('OllamaLLMBasePath', '${ollamaUrl}');
  console.log('Updated OllamaLLMBasePath to ${ollamaUrl}');
} catch (e) {
  console.error('Error:', e.message);
} finally {
  db.close();
}
`;

      try {
        fs.writeFileSync(scriptPath, scriptContent);
        await execPromise(`cd "${anythingPath}" && node psf-update-ollama-url.cjs`);
        fs.unlinkSync(scriptPath);
        console.log(`[AnythingLLM macOS ARM] ✅ Set OllamaLLMBasePath via Node script to ${ollamaUrl}`);
        return true;
      } catch (nodeErr) {
        console.log('[AnythingLLM macOS ARM] Node script failed:', nodeErr.message);
        try {
          fs.unlinkSync(scriptPath);
        } catch (_cleanupErr) {
          // no-op
        }
      }
    }

    return false;
  } catch (err) {
    console.error('[AnythingLLM macOS ARM] Error updating Ollama URL in database:', err.message);
    return false;
  }
}

