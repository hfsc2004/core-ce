let backendPort = null;
let frontendPort = null;
let ollamaPort = null;

/**
 * Kill a process and its entire process tree (Windows)
 */
async function killProcessTree(proc, name) {
  if (!proc) return;
  
  const pid = proc.pid;
  console.log(`[AnythingLLM Windows ARM64] Killing ${name} process tree (PID: ${pid})...`);
  
  try {
    await execPromise(`taskkill /F /T /PID ${pid}`);
    console.log(`[AnythingLLM Windows ARM64] Killed ${name} process tree ${pid}`);
  } catch (err) {
    console.log(`[AnythingLLM Windows ARM64] Process ${pid} may already be terminated`);
  }
}

/**
 * Kill any remaining yarn/node processes associated with AnythingLLM
 */
async function killRemainingAnythingLLMProcesses() {
  console.log('[AnythingLLM Windows ARM64] Cleaning up remaining AnythingLLM processes...');
  
  const processesToKill = ['yarn.cmd', 'yarn'];
  
  for (const procName of processesToKill) {
    try {
      await execPromise(`taskkill /F /IM ${procName} 2>nul`);
    } catch (err) {}
  }
  
  if (backendPort) {
    try {
      const { stdout } = await execPromise(`netstat -ano | findstr :${backendPort} | findstr LISTENING`);
      const lines = stdout.trim().split('\n');
      for (const line of lines) {
        const parts = line.trim().split(/\s+/);
        const pid = parts[parts.length - 1];
        if (pid && !isNaN(parseInt(pid))) {
          try {
            await execPromise(`taskkill /F /PID ${pid}`);
            console.log(`[AnythingLLM Windows ARM64] Killed process on backend port ${backendPort} (PID: ${pid})`);
          } catch (e) {}
        }
      }
    } catch (err) {}
  }
  
  if (frontendPort) {
    try {
      const { stdout } = await execPromise(`netstat -ano | findstr :${frontendPort} | findstr LISTENING`);
      const lines = stdout.trim().split('\n');
      for (const line of lines) {
        const parts = line.trim().split(/\s+/);
        const pid = parts[parts.length - 1];
        if (pid && !isNaN(parseInt(pid))) {
          try {
            await execPromise(`taskkill /F /PID ${pid}`);
            console.log(`[AnythingLLM Windows ARM64] Killed process on frontend port ${frontendPort} (PID: ${pid})`);
          } catch (e) {}
        }
      }
    } catch (err) {}
  }
}

/**
 * Create/update .env files in AnythingLLM directories with our dynamic ports
 */
